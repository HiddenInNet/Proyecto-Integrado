package hiddeninnet.proyectointegrado.api.dto;

import java.io.Serializable;
import java.util.Date;

public class UserDTO implements Serializable {

    private String username;
    private String name;
    private Date birthDate;
    private Date profileCreateDate;
    private String biography;
    private String email;
    private String lastName;
    private String password;
    private String phone;
    private byte[] profileImage;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getBirthDate() {
        return birthDate;
    }

    public void setBirthDate(Date birthDate) {
        this.birthDate = birthDate;
    }

    public Date getProfileCreateDate() {
        return profileCreateDate;
    }

    public void setProfileCreateDate(Date profileCreateDate) {
        this.profileCreateDate = profileCreateDate;
    }

    public String getBiography() {
        return biography;
    }

    public void setBiography(String biography) {
        this.biography = biography;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public byte[] getProfileImage() {
        return profileImage;
    }

    public void setProfileImage(byte[] profileImage) {
        this.profileImage = profileImage;
    }
}
